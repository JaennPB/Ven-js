// ========================================================================================
// ========================================================================  module imports

const LCD = require('raspberrypi-liquid-crystal');

// ========================================================================================
// ============================================================================ i2c address

const lcd = new LCD(1, 0x27, 16, 2);

// ========================================================================================
// ================================================================================ exports

lcd.beginSync();

exports.clearLCD = () => {
  lcd.clearSync();
  return;
};

exports.printLCD = (line, text) => {
  lcd.printLineSync(line, text);
  return;
};
